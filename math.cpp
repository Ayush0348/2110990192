#include <iostream>
using namespace std;
#include <cmath>

int main(){
     cout << "The max out of 5 and 10 is " << max(5,10) << endl;
     cout << "The min out of 5 and 10 is " << min(5,10) << endl;
     cout << "The square root of 49 is " << sqrt(49) << endl;
     cout << "The nearest round integer of -7.2 is " << round(-7.2) << endl;
     cout << "The log of 147 is " << log(147) << endl;
     
     return 0;
}

